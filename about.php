<ul>
<li><strong>&Ouml;d&uuml;l bazlı kitle fonlaması kısaca nedir? </strong></li>
</ul>
<p>&nbsp; Bu kitle fonlama modelinde proje sahibi belirlediği &ouml;d&uuml;lleri kendisini destekleyen kişilere g&ouml;ndermektedir. &Ouml;d&uuml;ller proje sahibinin tercihine g&ouml;re belirlendiği i&ccedil;in projeden projeye farklılık g&ouml;stermektedir. Bu modelin daha iyi anlaşılması i&ccedil;in bir &ouml;rnek vermek istiyoruz. Proje sahibi kendisini 10 TL destekleyen kişilere eğer ortaya &ccedil;ıkaracağı &uuml;r&uuml;n&uuml; g&ouml;ndermek istiyorsa; 10 TL desteğiniz karşılığında belirtilen tarihte proje sahibinin ortaya &ccedil;ıkardığı &uuml;r&uuml;ne sahip olursunuz.</p>
<p>&nbsp;</p>
<ul>
<li><strong>Fikir Bah&ccedil;ıvanı&rsquo;na herkes proje yollayabilir mi? </strong></li>
</ul>
<p>&nbsp; Buna cevabımız bir m&uuml;ddet boyunca bir sosyal sorumluluk projeniz var ise evet, diğer projeler i&ccedil;inse sadece &ouml;ğrencilerin projelerini değerlendirmekteyiz. Yaşı 18&rsquo;den k&uuml;&ccedil;&uuml;k olan &ouml;ğrenciler ise veli izni ile birlikte proje yollayabilirler.</p>
