<p>&nbsp; Kitle fonlama sistemi bizim i&ccedil;in yeni bir heyecan. Daha &ouml;nce platform olarak girişimcilik ekosistemi hakkında bilgi veren, video &ccedil;ekimleri yapan ve girişimcilerle r&ouml;portaj yapan bir platformduk. Daha sonra girişimcilik ekosistemi i&ccedil;erisinde &ouml;ğrencilerin yaşadığı sorunları g&ouml;rmemiz ile birlikte, &ouml;ğrenciler i&ccedil;in &ouml;d&uuml;l bazlı kitle fonlama sistemine ge&ccedil;tik. 3 yıldır edindiğimiz bilgi ve tecr&uuml;be ile şu an kitle fonlama platformu olarak karşınızdayız biraz ge&ccedil; kaldığımızın farkındayız bu durumun asıl sebebi ise ekonomik sebepler bundan kaynaklı daha hızlı ilerlememiz ve daha &ccedil;ok &ouml;ğrencinin hayatına dokunabilmemiz i&ccedil;in sizlerden Fikir Bah&ccedil;ıvanı i&ccedil;in desteklerinizi bekliyoruz. Aynı zamanda bir kitle fonlaması değil aynı zamanda girişimcilerin ortaya &ccedil;ıkardığı &uuml;r&uuml;nleri satabileceği (Manav), kendilerini geliştirmek i&ccedil;in eğitimler alabileceği (Bu sayfamız şu an yapım aşamasında) bir yapıyız da. Zaman i&ccedil;erisinde platformumuzu daha iyi hale getirerek farklı alanlarda da hizmet vereceğiz. Fakat şu anlık kitle fonlamasına odaklandık ve sistemi daha iyi hale getirmek i&ccedil;in t&uuml;m g&uuml;c&uuml;m&uuml;zle &ccedil;alışmalar s&uuml;rd&uuml;rmekteyiz. &nbsp;</p>
